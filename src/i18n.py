class I18n:
    """Internationalization helper class."""

    def __init__(self, lang: str = "en"):
        self._lang = lang if lang in LANGUAGES else "en"

    @property
    def lang(self) -> str:
        return self._lang

    @lang.setter
    def lang(self, value: str):
        if value in LANGUAGES:
            self._lang = value

    def t(self, key: str, **kwargs) -> str:
        """Get translated string for the current language.

        Args:
            key: Translation key
            **kwargs: Format parameters (e.g. count=5, file="test.cbl")

        Returns:
            Translated and formatted string
        """
        entry = TRANSLATIONS.get(key)
        if entry is None:
            return f"[{key}]"

        text = entry.get(self._lang) or entry.get("en", f"[{key}]")

        if kwargs:
            try:
                text = text.format(**kwargs)
            except (KeyError, IndexError):
                pass

        return text

    @staticmethod
    def available_languages() -> dict:
        """Return dict of {code: display_name}."""
        return dict(LANGUAGES)
